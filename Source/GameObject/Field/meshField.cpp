void MeshField::Init()
{
	// 頂点バッファ生成
	{
		for (int x = 0; x < FIELD_X; x++)
		{
			for (int z = 0; z < FIELD_Z; z++)
			{
				float y = g_FieldHeight[x][z];

				m_Vertex[x][z].Position =
					D3DXVECTOR3((x - 10) * 5.0f, y, (z - 10) * -5.0f);
				m_Vertex[x][z].Normal = D3DXVECTOR3(0.0f, 1.0f, 0.0f);
				m_Vertex[x][z].Diffuse = D3DXVECTOR4(1.0f, 1.0f, 1.0f, 1.0f);
				m_Vertex[x][z].TexCoord = D3DXVECTOR2(x * 0.5f, z * 0.5f);
 			}
		}


		// 法線ベクトル算出
		for (int x = 1; x <= FIELD_X - 2; x++)
		{
			for (int z = 1; z <= FIELD_Z - 2; z++)
			{
				D3DXVECTOR3 vx, vz, vn;
				vx = m_Vertex[x + 1][z].Position - m_Vertex[x - 1][z].Position;
				vz = m_Vertex[x][z - 1].Position - m_Vertex[x][z + 1].Position;

				D3DXVec3Cross(&vn, &vz, &vx); // 外積
				D3DXVec3Normalize(&vn, &vn);  // 正規化(長さ1にする)
				m_Vertex[x][z].Normal = vn;
			}
		}

		D3D11_BUFFER_DESC bd;
		ZeroMemory(&bd, sizeof(bd));
		bd.Usage = D3D11_USAGE_DEFAULT;
		bd.ByteWidth = sizeof(VERTEX_3D) * FIELD_X * FIELD_Z;
		bd.BindFlags = D3D11_BIND_VERTEX_BUFFER;
		bd.CPUAccessFlags = 0;

		D3D11_SUBRESOURCE_DATA sd;
		ZeroMemory(&sd, sizeof(sd));
		sd.pSysMem = m_Vertex;

		Renderer::GetDevice()->CreateBuffer(&bd, &sd, &m_VertexBuffer);
	}

	// インデックスバッファ作成
	{
		unsigned int index[(22 * 2) * 20 - 2];

		int i = 0;
		for (int x = 0; x < FIELD_X - 1; x++)
		{
			for (int z = 0; z < FIELD_Z; z++)
			{
				index[i] = x * FIELD_X + z;
				i++;

				index[i] = (x + 1) * FIELD_X + z;
				i++;
			}

			if (x == FIELD_X-2)
				break;

			index[i] = (x + 1) * FIELD_X + (FIELD_X-1);
			i++;

			index[i] = (x + 1) * FIELD_X;
			i++;
		}

		D3D11_BUFFER_DESC bd;
		ZeroMemory(&bd, sizeof(bd));
		bd.Usage = D3D11_USAGE_DEFAULT;
		bd.ByteWidth = sizeof(unsigned int) * ((FIELD_X+1) * 2) * (FIELD_X-1) - 2;
		bd.BindFlags = D3D11_BIND_INDEX_BUFFER;
		bd.CPUAccessFlags = 0;

		D3D11_SUBRESOURCE_DATA sd;
		ZeroMemory(&sd, sizeof(sd));
		sd.pSysMem = index;

		Renderer::GetDevice()->CreateBuffer(&bd, &sd, &m_IndexBuffer);
	}


	m_position = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
	m_rotation = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
	m_scale = D3DXVECTOR3(1.0f, 1.0f, 1.0f);

	
		// テクスチャ読み込み
		D3DX11CreateShaderResourceViewFromFile(Renderer::GetDevice(),
			"asset/texture/beach.jpg",
			NULL,
			NULL,
			&m_Texture,
			NULL);
		assert(m_Texture);

		

}


